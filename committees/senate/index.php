            <h3>Welcome to the United States Senate at KINGMUN 2018. As this committee will be unique and complex, it is an advanced committee meant for delegates who are quick-learners, flexible, and focused. Preparation will be key to success in this simulation, as the unique Rules of Procedure pose a challenge to even the most seasoned delegates. In committee, expect engaging debate about some of the complex problems facing the United States. In order to solve these critical issues, comprehensive solutions must be developed through bipartisan cooperation. Senators must cross the aisle and work together, seeing past party lines, to be successful. Delegates who can think outside of the box and produce creative ideas will thrive in the U.S. Senate.</h3>
            <h3>To prepare, delegates will write a bill (see below for specifics). Prior to the conference, staff will review bills and assemble four separate dockets. The first session will consist of mini-committee sessions, where the committee will split into four groups, and every bill is heard before their committee. From there, successful bills (i.e. those that pass), will be assembled into a new docket by the staff, and heard in front of the entire Senate. The procedure of Senate is complicated, and delegates wishing to know more specific information than that provided for online should contact the dais.</h3>
            <h3>For this committee, both a position paper and a bill is required. Position papers should total one page in length and discuss your Senator’s party alignment and views on both topics. In addition to this, each delegate must submit one bill on the theme that they are assigned. Delegates are encouraged to write bills on the subtopics summarized in the background guide, but are not required to. However, their bill must pertain to the theme they were assigned. For example, a delegate assigned to environmental sustainability may choose to forgo the subtopics and write about implementing desalination plants to ensure water security, but may not write about banning tide-pods in the United States. In all, each delegate will submit two pages, one page consisting of a position paper, and one consisting of their bill (bills may go over one page, but should not be more than two). </h3>
