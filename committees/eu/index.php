            <h2 style="text-align: left">Topic 2: Single Monetary Union</h2>
            <h3>Though one of the European Union’s main areas of focus is economics, there exists an ongoing debate over the Eurozone’s Single Monetary Policy. The shared monetary policy between Euro Area members is meant to promote integration of nations’ economies as well as increase confidence in the international economy. Having a single, shared currency eliminates exchange rates and costs and makes it easier for companies to trade throughout the Union, but countries have to meet certain criteria to enter the Eurozone.  However, by adopting the Euro, nations give up a significant amount of their national authority and independence over their economies. Some countries are extremely sensitive to exchange rates and other countries that currently are in large economic declines would be taking large risks by adopting the Euro. With about two thirds of EU member states sharing a currency, the Euro Area is the only international economic monetary union and is predicted to continue expanding. The economic and political effects of a single monetary policy is now an international matter becoming more prevalent to European nations.
            </h3>
