            <h3>The World Health Organization (WHO) was established on April 7th, 1948, and is currently headquartered in Geneva, Switzerland. With 194 member countries and working in approximately 150 countries, the WHO directs and coordinates different programs and funds to “build a better, healthier future for people all over the world.” A notable objective of the WHO is to work with other UN agencies and non-governmental organizations to solve complex and multifaceted health issues on the global scale. Having been successful in many of its endeavors, the WHO has directed its focus on six main priorities. These include advancing universal health coverage, combating communicable and noncommunicable diseases, addressing the challenge of mental health issues, expanding countries’ abilities to respond to public health threats, working towards equal access to medical products, and promoting education about health issues. As the WHO, “together we strive to combat diseases... ensure the safety of the air people breathe, the food they eat, the water they drink, and the medicines and vaccines they need.”
