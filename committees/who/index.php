            <h2 style="text-align:left">Topic 1: Water Sanitation in Africa</h2>
            <h3>Essential to life, water sanitation is a growing issue with the amount of available drinking water slowly diminishing. This committee will focus on the issues associated with the lack of drinking water, with a special interest in the current situation on the African continent. 2.1 billion people are unable to access clean drinking water and water scarcity affects four out of ten people world wide. With climate change on the rise, this issue has become even more prevalent. Keeping in mind that the United Nations made it a human right to have access to drinkable water, this committee will need to work hard to create feasible solutions.
            </h3>
            <h2 style="text-align: left">Topic 2: Women’s Reproductive rights</h2>
            <h3>Often highlighted in our news, women’s rights is a very pressing issue that needs to be addressed immediately. Although there is a wide spectrum, this committee will take a special interest in the provision of facilities to women in need of reproductive services such as abortion clinics, sanitary products, and safe spaces for giving birth. Many countries do not provide such services and due to both funding and social prejudice against the issue, it often gets overlooked in governments. This may prove difficult for this committee, compromise will be very important in finding a solution.
            </h3>
