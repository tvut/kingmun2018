            <h1 style="font-size:40px;padding-bottom:20px;">Frequently Asked Questions</h1>
            <h2>When does registration open?</h2>
            <h3 class="no-indent">Registration opens February 12, 2018. More information about registration will be
                available <a class="main" href="../registration/">here</a>.</h3>
            <h2>Who can register for KINGMUN?</h2>
            <h3>All students attending high school may register to attend KINGMUN through their school’s MUN club or as
                independent delegates.</h3>
            <h2>Must I register through my school, or can I register individually?</h2>
            <h3>All delegates must register via their school’s MUN club to be admitted to KINGMUN if their school's MUN
                club is in attendance. Each club’s advisor or head delegate should register their delegation. Then, each
                MUN club will receive country assignments for its entire delegation. Those who do not have a club or
                who’s club is not attending KINGMUN may register independently but must have a chaperone. These cases
                will be handled on a individual basis so email <a href="mailto:sg@kingmun.org">sg@kingmun.org</a> if
                this topic pertains to you.</h3>
            <h2>What committees are available?</h2>
            <h3 class="no-indent">There are eight committees to choose from including the Joint Crisis Committee,
                Historical Crisis Committee, United Nations Human Rights Council, World Health organization, the US
                Senate, United Nations Environmental Programme, United Nations Office for Outer Space Affairs, and the
                European Union. More information on committees is available <a href="../committees/">here</a>.</h3>
            <h2>What is the dress code for KINGMUN?</h2>
            <h3>All delegates must wear Western business attire. For male delegates this includes a dress shirt, dress
                pants, and a tie. Female delegates may choose from a wider array of clothing, provided that they project
                a professional image. Both sexes are encouraged, but not required, to wear a business jacket.</h3>
            <h3>Delegates who do not abide by the dress code may be asked to change and be suspended from the conference
                until they dress appropriately.</h3>
            <h2>Will food be provided?</h2>
            <h3>The KINGMUN Secretariat will not provide any meals. Delegates are urged to go bring their own or make
                use of the 90 minute lunch period to order food. KINGMUN is roughly a 15 minute walk from downtown
                Kirkland, which offers a variety of restaurant options.</h3>
