            <h1 style="font-size:40px;padding-bottom:20px;">Conference Policy</h1>
            <h2>Refund Policy</h2>
            <h3>No refunds will be given unless the conference does not occur or the cancellation arises from medical
                issues, force majeure, or other circumstances arising beyond reasonable control. These cases will be
                reviewed on an individual basis.</h3>
            <h2>Technology Policy</h2>
            <h3>Technology is a unique and integral part of KINGMUN. At the beginning of the conference, all delegates
                will receive USB storage drives containing, among other items, committee-specific documents (background
                guides, position papers, crisis items), Rules of Procedure specifications, and the Delegate Handbook. In
                order to fully experience KINGMUN, delegates are <b>strongly urged</b> to bring personal computing
                devices capable of accessing the storage drive. In the event that a delegate cannot use the storage
                drive, hard copies will be provided.</h3>
            <h3>The use of phones is not permitted during committee session. The use of other internet-connected devices
                is permitted during informal debate (moderated and unmoderated caucuses) <b>only</b>.</h3>
            <h3>If a dais member feels that a delegate is abusing the right to use an electronic device, the dais member
                will issue a warning to the delegate. If such activity persists, the incident will be reported to a
                Secretariat member, who will take corrective action including, but not limited to, restricting the
                offending delegate’s usage of electronic devices. If further issues arise, the adviser responsible for
                the delegate will be contacted.</h3>
            <h2>Rooming Policy</h2>
            <h3>Unfortunately, KINGMUN is not able to offer any rooming for Saturday night. This also means that KINGMUN
                claims no responsibility, legal or otherwise, for the whereabouts or activities of KINGMUN delegates
                in-between committee sessions.</h3>
